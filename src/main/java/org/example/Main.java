package org.example;


import java.util.Scanner;

public class Main {
        public static void main(String[] args) {

            Scanner sc= new Scanner(System.in);   // 입력

            int A, B;   // int타입 A,B 선언
            A = sc.nextInt();   // A의 값을 정수로 변환 / A = 1
            B = sc.nextInt();   // B의 값을 정수로 변환 / B = 2


            System.out.println(A * B); //출력 : 2
        }
    }














